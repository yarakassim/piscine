void	ft_rev_int_tab(int *tab, int size)
{
	int t;
	int c;
	int s;

	c = 0;
	s = (size - 1);
	while (c != size / 2)
	{
		t = tab[c];
		tab[c] = tab[s - c];
		tab[s - c] = t;
		c++;
	}
}
