void	ft_sort_int_tab(int *tab, int size)
{
	int c;
	int t;

	c = 0;
	t = 0;
	while (c < size)
	{
		if (tab[c] >= tab[c + 1])
		{
			t = tab[c];
			tab[c] = tab[c + 1];
			tab[c + 1] = t;
			c = 0;
		}
		else
			c++;
	}
}
