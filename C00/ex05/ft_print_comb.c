#include <unistd.h>

void	ft_putchar(char n)
{
	write(1, &n, 1);
}

void	dis(char a, char b, char c)
{
	ft_putchar(a);
	ft_putchar(b);
	ft_putchar(c);
	if (a != '7')
		write(1, ", ", 2);
}

void	ft_print_comb(void)
{
	char a;
	char b;
	char c;

	a = '0';
	b = '0';
	c = '0';
	while (a <= '7')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				dis(a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
}
